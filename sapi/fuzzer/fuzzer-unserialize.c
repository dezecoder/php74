int LLVMFuzzerTestOneInput(const uint8_t *Data, size_t Size) {
	zval arg;

	if (fuzzer_request_startup()==FAILURE) {
		return 0;
	}

	ZVAL_STRINGL(&arg, (const char *) Data, Size);
	fuzzer_call_php_func_zval("unserialize", 1, &arg);
	zval_ptr_dtor(&arg);

	php_request_shutdown(NULL);

	return 0;
}

